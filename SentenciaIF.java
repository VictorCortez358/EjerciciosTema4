public class SentenciaIF {
    public static void main(String[] args) {
        int numeroIf= 0;

        if (numeroIf > 0){
            System.out.println("El numero es positivo");
        } else if (numeroIf < 0) {
            System.out.println("El numero es negativo");
        } else if (numeroIf == 0) {
            System.out.println("El numero es cero");
        }
    }
}
